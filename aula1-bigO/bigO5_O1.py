def add_items(n):  # Aqui temos uma operação apenas.
    return n+n  # Se n for 1 trilhão, ainda sim teremos apenas 1 operação computacional.
# Portanto, O(1). Gráfico de operações é constante, reto e plano vs número n crescente.

print(add_items(2))

# É o algoritmo mais otimizado possível. Quando se faz apenas 1 operação que resolve um problema.
